                          <p>Arauca recibe, por primera vez, las competencias regionales de Supérate, Del 14 al 25 de agosto, Arauca recibirá por primera vez el regional Llanos del programa Supérate Intercolegiados, evento que convoca los campeones departamentales de las justas escolares de siete departamentos del oriente y la amazonía colombiana.</p>
                          <p>Más de 1.800 jóvenes entre los 13 y 17 años, se dan cita en Arauca. Divididos en dos categorías, prejuvenil y juvenil, en los deportes baloncesto, fútbol, fútbol sala, fútbol de salón y voleibol, buscarán el cupo a la gran final nacional de Supérate Intercolegiados en el Valle del Cauca, del 6 al 27 de octubre próximos.</p>
						  <p>Las delegaciones de los siete departamentos que conforman la región – Amazonas, Casanare, Guanía, Guaviare, Vaupés, Vichada y el local Arauca- competirán en el Coliseo Los Fundadores, el Polideportivo Corocoras, el estadio municipal de fútbol, el colegio General Santander, el colegio Simón Bolívar y el Colegio Francisco José de Caldas sede Juan Francisco Lara.</p>
						  <p>El talento viene de todos los rincones de Colombia, ya que Supérate llega a la totalidad de municipios del territorio nacional (1.102). Un total de 46 instituciones educativas de la región serán representadas en estas competencias por sus estudiantes, que vienen de lugares como Cumaribo, Vichada; Carurú, Vaupés; Miraflores, Guaviare; Fortul, Arauca; además de las capitales de cada departamento.</p>
						  <p>Este evento deportivo ratifica el compromiso de Coldeportes de llegar a todas las regiones, dinamizando la economía del municipio en cuanto a hotelería, transporte, gastronomía y mano de obra. Además, fortalece los principios del programa, que dentro de sus iniciativas propicia que los jóvenes conozcan las diversas regiones y culturas colombianas.</p>
